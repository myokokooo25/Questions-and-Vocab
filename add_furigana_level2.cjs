function addFurigana(text) {
  if (!text) return text;
  let processedText = text;
  for (const [kanji, furigana] of Object.entries(kanjiFuriganaMap)) {
    // Only replace kanji not already wrapped in ruby
    const regex = new RegExp(`(?<!<ruby>)${kanji}(?!<rt>)`, 'g');
    processedText = processedText.replace(regex, `<ruby>${kanji}<rt>${furigana}</rt></ruby>`);
  }
  return processedText;
}

