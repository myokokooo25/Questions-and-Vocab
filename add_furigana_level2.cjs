const filename = args[0] || 'data/chapter3-2026-level2-part1.ts';

if (fs.existsSync(filename)) {
  console.log(`Processing ${filename}...`);
  let content = fs.readFileSync(filename, 'utf-8');
  
  // Quick and dirty regex to find questionJP and textJP values and add furigana
  content = content.replace(/"questionJP":\s*"(.*?)"/g, (match, p1) => {
    return `"questionJP": "${addFurigana(p1)}"`;
  });
  
  content = content.replace(/"textJP":\s*"(.*?)"/g, (match, p1) => {
    return `"textJP": "${addFurigana(p1)}"`;
  });

  fs.writeFileSync(filename, content, 'utf-8');
  console.log(`Updated ${filename}`);
} else {
  console.log(`File not found: ${filename}`);
}
